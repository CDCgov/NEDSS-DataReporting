-- Patient 1: race fields propagate correctly to MORBIDITY_REPORT_DATAMART
SELECT
    MORBIDITY_REPORT_KEY,
    RACE_CALCULATED,
    RACE_CALCULATED_DETAILS
FROM dbo.MORBIDITY_REPORT_DATAMART
WHERE MORBIDITY_REPORT_KEY = 90100002;

